import Ros7
hammingDist = Ros7.hammingDistance

def distanceBetweenPatternString(pattern, dna):
    k = len(pattern)
    distance = 0
    for text in dna:
        hamming_distance = float('inf')
        all_kmers = []
        for i in range (len(text) - k + 1):
            all_kmers.append(text[i:i+k])
        for kmer in all_kmers:
            if hamming_distance > hammingDist(pattern, kmer):
                hamming_distance = hammingDist(pattern, kmer)
        distance = distance + hamming_distance
    return distance

